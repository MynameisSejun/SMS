    public void setBill(String stdnum) {
        File file = new File("studentclasses.txt");
        try (BufferedReader br = new BufferedReader(new InputStreamReader(new FileInputStream(file), "UTF-8"))) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            String temp;
            while ((temp = br.readLine()) != null) {
                String[] dataRow = temp.split(",");

                if (dataRow[0].equals(stdnum)) {
                    String inputStr[] = new String[6];

                    inputStr[0] = dataRow[2];    //강좌번호
                    inputStr[1] = dataRow[3];    //강좌명
                    inputStr[2] = dataRow[4];    //학점
                    inputStr[3] = (Integer.parseInt(dataRow[4]) * 45000) + "";
                    sum += Integer.parseInt(dataRow[4]) * 45000;

                    model.addRow(inputStr);
                    jTextField6.setText(Integer.toString(sum));

                }
            }
        } catch (IOException ex) {
            ex.getStackTrace();
        }
    }

