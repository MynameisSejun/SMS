    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String filePath = "classmanagerInfo.txt";
        ClassManager manager = new ClassManager(ID.getText(), PW.getText());
        MCheck ck = new MCheck();
        
        boolean idCheck = true;
        boolean emCheck = true;
        
        emCheck = ck.emptyMCheck(ID.getText(), PW.getText());
        idCheck = ck.equalCehck(filePath, ID.getText());
        
        if(emCheck) {
            if(idCheck) {
                try (BufferedWriter writer = new BufferedWriter(new FileWriter(filePath, true))) {
                String managerInfo = manager.getID() + "," + manager.getPW();
                writer.write(managerInfo);
                writer.newLine();
                JOptionPane.showMessageDialog(null, "정상 등록되었습니다.");
                dispose();
                SM_FirstPage sf = new SM_FirstPage();
                sf.setVisible(true);
            } catch (IOException e) {
                e.printStackTrace();
            }
            }else {
            JOptionPane.showMessageDialog(null, "중복된 수업 관리자 번호가 있습니다.");
            }
        }else {
            JOptionPane.showMessageDialog(null, "빈칸이 있습니다. 마저 입력해주세요.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

