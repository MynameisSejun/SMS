    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String filePath = "studentInfo.txt";
        Student student = new Student(sName.getText(), sNum.getText(), sMajor.getSelectedItem(), sSecretnum.getText(), sSecretnum2.getText());
        Check ck = new Check();
        
        boolean idCheck = true;
        boolean emCheck = true;
        
        emCheck = ck.emptyCheck(sName.getText(), sNum.getText(), sMajor.getSelectedItem(), sSecretnum.getText(), sSecretnum2.getText());
        idCheck = ck.equalCehck(filePath, sNum.getText());
        
        if(emCheck) {
            if(idCheck) {
                try (BufferedWriter bw = new BufferedWriter(new OutputStreamWriter(new FileOutputStream(filePath, true), "UTF-8"))) {
                    String scNum = student.getSecretNum() + "-" + student.getSecretNum2();
                    String studentInfo = student.getStudentId() + "," + student.getSecretNum2() + "," + student.getName() + "," + student.getMajor() + "," + scNum;
                    //if (check == false) 
                    bw.write(studentInfo);
                    bw.newLine();
                    JOptionPane.showMessageDialog(null, "저장되었습니다!");
                    //else if(check == true) 빈칸 있음
                    dispose();
                    SM_FirstPage sf = new SM_FirstPage();
                    sf.setVisible(true);
                } catch (IOException e) {
                    e.printStackTrace();
                }
            } else {
                JOptionPane.showMessageDialog(null, "중복된 학생 번호가 있습니다.");
            }
        }else {
            JOptionPane.showMessageDialog(null, "빈칸이 있습니다. 마저 입력해주세요.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

